package codetraining.ct05_maaslar;

public class Mudur extends Calisan {

    public Mudur() {
    }

    public Mudur(double maas) {
        super(maas);
    }

    @Override
    public double getirMaas() {
        return super.getirMaas();
    }

    public String toString() {
        return "Mudur Maasi: " + getirMaas();
    }
}
